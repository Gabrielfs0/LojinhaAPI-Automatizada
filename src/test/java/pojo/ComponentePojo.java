package pojo;

public class ComponentePojo {
    private String nomeComponente;
    private int quantidadeComponente;

    public String getNomeComponente() {
        return nomeComponente;
    }

    public void setNomeComponente(String nomeComponente) {
        this.nomeComponente = nomeComponente;
    }

    public int getQuantidadeComponente() {
        return quantidadeComponente;
    }

    public void setQuantidadeComponente(int quantidadeComponente) {
        this.quantidadeComponente = quantidadeComponente;
    }
}
